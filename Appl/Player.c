#define BUNKERW     ((unsigned char)Bunker0[18])
#define BUNKERH     ((unsigned char)Bunker0[22])
#define PLAYERW     ((unsigned char)PlayerShip0[18])
#define PLAYERH     ((unsigned char)PlayerShip0[22])
#define SCREENXMAX 84
#define SCREENYMAX 48

unsigned long PlayerPosition;
unsigned long PlayerAlive;
unsigned long PlayerExplosion;
unsigned long PlayerLives;

/**
 * @brief:
 * @param:
 * @return:
**/
void Player_Init(void)
{
    PlayerPosition = 33;
    PlayerAlive = 1;
    PlayerExplosion = 0;
    PlayerLives = 2;
}

/**
 * @brief:
 * @param:
 * @return:
**/
void Player_Draw(void)
{
    if (PlayerAlive) {
        Nokia5110_PrintBMP(PlayerPosition, SCREENYMAX - 1, PlayerShip0, 0);
    } else {  // PlayerExplosion
        Nokia5110_PrintBMP(PlayerPosition, SCREENYMAX - 1, Random()%2 ?
                                                           BigExplosion1 : BigExplosion0
                                                         , 0);
        PlayerExplosion--;
    }
}

/**
 * @brief:
 * @param:
 * @return:
**/
void Player_Kill(unsigned long xpos, unsigned long ypos)
{
    if (xpos > (PlayerPosition) && xpos < (PlayerPosition + PLAYERW)) {
       PlayerAlive = 0;
       PlayerExplosion = 40;
    }
}

/**
 * @brief:
 * @param:
 * @return:
**/
void Player_Revive(void)
{
    PlayerPosition = 33;
    PlayerAlive = 1;
    PlayerExplosion = 0;
}

/**
 * @brief:
 * @param:
 * @return:
**/
void Player_Draw_Bunker(void)
{
    Nokia5110_PrintBMP((SCREENXMAX-BUNKERW)/2, SCREENYMAX - PLAYERH, Bunker0, 0);
}


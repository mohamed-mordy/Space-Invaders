#define MISSILEW    ((unsigned char)Missile0[18])
#define MISSILEH    ((unsigned char)Missile0[22])
#define MAXMISSILES 30

struct Missiles {
    unsigned long head;
    unsigned long tail;
    unsigned long count;
    struct Missile {
        unsigned long x;
        unsigned long y;
        const unsigned char *image;
    } missiles[MAXMISSILES];
} Missiles;

/**
 * @brief:
 * @param:
 * @return:
**/
void Missile_Init(void)
{
    Missiles.head = 0;
    Missiles.tail = 0;
    Missiles.count = 0;
}

/**
 * @brief:
 * @param:
 * @return:
**/
// Only one usage per frame, otherwise, the behavior is undefined
void Missile_Fire(unsigned long xpos, unsigned long ypos)
{
    if (Missiles.count < 30) {
        Missiles.missiles[Missiles.head].x = xpos;
        Missiles.missiles[Missiles.head].y = ypos;
        Missiles.missiles[Missiles.head].image = Random()%2? Missile0 : Missile1;
        Missiles.head = (Missiles.head + 1) % MAXMISSILES;
        Missiles.count++;
    }
}

/**
 * @brief:
 * @param:
 * @return:
**/
void Missile_Move(void)
{
    unsigned long i, j;
    for (i = 0, j = Missiles.tail; i < Missiles.count; j = (j+1) % MAXMISSILES, i++) {
        Missiles.missiles[j].y++;
    }
}


/**
 * @brief: returns the number of missiles in the missiles queue
 * @param: none
 * @return: number of missiles in the missiles queue
**/
unsigned long Missile_Count(void)
{
    return Missiles.count;
}

/**
 * @brief: draws the missiles in the screen buffer
 * @param: none
 * @return: none
**/
void Missile_Draw(void)
{
    unsigned long i, j;
    for (i = 0, j = Missiles.tail; j < Missiles.count; j = (j+1) % MAXMISSILES, i++) {
        Nokia5110_PrintBMP(Missiles.missiles[j].x,
                           Missiles.missiles[j].y,
                           Missiles.missiles[j].image,
                           0);
    }
}

/**
 * @brief: returns the Y coordinate of the lowest Missile
 * @param: none
 * @return: Y coordinate of the lowest Missile
**/
unsigned long Missile_Lowest_YPos(void)
{
    return Missiles.missiles[Missiles.tail].y;
}

/**
 * @brief: returns the X coordinate of the lowest Missile
 * @param: none
 * @return: X coordinate of the lowest Missile
**/
unsigned long Missile_Lowest_XPos(void)
{
    return Missiles.missiles[Missiles.tail].x;
}

/**
 * @brief: removes the lowest missile from the queue of missiles
 *         and accordingly clears it from the screen
 * @param: none
 * @return: none
**/
void Missile_Demise_Lowest(void)
{
    if (Missiles.count > 0) {
        Missiles.tail = (Missiles.tail + 1) % MAXMISSILES;
        Missiles.count--;
    }
}


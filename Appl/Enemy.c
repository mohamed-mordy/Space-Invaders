/**
 * @brief:
 * @param:
 * @return:
**/
void Enemy_Draw(unsigned long frame)
{
    unsigned long i, j;
    for (i = 0; i < 10; i++) {
        if (Enemies[i].live) {
            Nokia5110_PrintBMP(Enemies[i].x,
                               Enemies[i].y,
                               frame? SmallEnemy10PointA : SmallEnemy10PointB,
                               0);
        } else if (Enemies[i].explosion) {
            Nokia5110_PrintBMP(Enemies[i].x,
                               Enemies[i].y,
                               SmallExplosion0,
                               0);
            Enemies[i].explosion--;
        } else {
            Nokia5110_PrintBMP(Enemies[i].x,
                               Enemies[i].y,
                               SmallExplosion1,
                               0);
        }
    }
}

/**
 * @breif:
 * @param:
 * @return:
**/
void Enemy_Kill(unsigned long xpos, unsigned long ypos)
{
    unsigned long x = 0;
    if (xpos >= 0 && xpos <= 16) {
        x = 5;
    } else if (xpos >= 17 && xpos <= 32) {
        x = 6;
    } else if (xpos >= 33 && xpos <= 48) {
        x = 7;
    } else if (xpos >= 49 && xpos <= 66) {
        x = 8;
    } else if (xpos >= 67 && xpos <= 83) {
        x = 9;
    }
    
    if (x && Enemies[x].live) {
        Enemies[x].live = 0;
        Enemies[x].explosion = 10;
    }
}

